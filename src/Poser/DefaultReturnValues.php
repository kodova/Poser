<?php

namespace Poser;


class DefaultReturnValues {
	
	public function returnZero() {
		return 0;	
	}
	
	public function returnNull() {
		return null;	
	}
	
	public function returnFalse() {
		return false;
	}
	
	public function returnString() {
		return '';
	}
	
	public function returnArray() {
		return array();
	}
}
